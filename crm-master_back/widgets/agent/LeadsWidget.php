<?php
namespace app\widgets\agent;

use yii\base\Widget;
use yii\helpers\Html;

class LeadsWidget extends Widget
{
    public $message;

    public function init()
    {
        parent::init();
        if ($this->message === null) {
            $this->message = 'Hello World';
        }
    }

    public function run()
    {
        return Html::encode($this->message);
    }
}